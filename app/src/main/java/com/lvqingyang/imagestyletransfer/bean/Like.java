public class Like extends BmobObject {
    private User user;
    private Picture pic;

    public Like(User user, Picture pic) {
        this.user = user;
        this.pic = pic;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Picture getPic() {
        return pic;
    }

    public void setPic(Picture pic) {
        this.pic = pic;
    }
}
