public class Platform {
    private int imgId;
    private String name,packet,activity;


    public Platform(int imgId, String name, String packet, String activity) {
        this.imgId = imgId;
        this.name = name;
        this.packet = packet;
        this.activity = activity;
    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPacket() {
        return packet;
    }

    public void setPacket(String packet) {
        this.packet = packet;
    }

    public String getActivity() {
        return activity;
    }

    public void setActivity(String activity) {
        this.activity = activity;
    }
}
